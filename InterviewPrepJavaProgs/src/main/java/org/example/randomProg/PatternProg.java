package org.example.randomProg;

public class PatternProg {
    public static void main(String[] args) {
        int k=5,l=5;
        for(int i=0;i<k;i++){
            for(int j=i;j<l;j++){
                System.out.println("*");

            }
            System.out.println(" ");
        }
    }
}
